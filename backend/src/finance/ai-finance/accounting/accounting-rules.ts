import { FinOpType, AccountingEffect } from '@prisma/client';

// Единый источник истины доменных правил учёта (§8 требований).
// AI обязан их соблюдать; правила зашиты и в system-промпт.

// Типы операций, которые ВСЕГДА нейтральны (не влияют на P&L).
export const NEUTRAL_OP_TYPES = new Set<FinOpType>([
  'TRANSFER',
  'LOAN_PRINCIPAL',
  'DEPOSIT_PLACE',
  'DEPOSIT_RETURN',
  'DIVIDEND',
]);

// Ключевые слова-сигналы нейтральных операций (для rule-engine и промпта).
export const NEUTRAL_KEYWORDS = [
  'перевод', 'transfer', 'аванс', 'предоплата', 'депозит',
  'залог', 'кредит', 'тело кредита', 'возврат залога', 'пополнение счёта',
];

export function effectFromOpType(type: FinOpType, isPnl: boolean): AccountingEffect {
  if (NEUTRAL_OP_TYPES.has(type)) return 'NEUTRAL';
  if (!isPnl) return 'DDS_ONLY';
  return 'DDS_AND_PNL';
}

